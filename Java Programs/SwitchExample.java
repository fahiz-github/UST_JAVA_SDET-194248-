package com.ust.test;

public class SwitchExample {
	
	public static void main(String[] args)
	{
		String s="apple";
		switch(s) {
			
		case "mango":
			System.out.println("its mango");
			break;
			
		case "apple":
			System.out.println("its apple");
			break;
			
		case "orange":
			System.out.println("its orange");
			break;
			default:
				System.out.println("its default");
			
			
		}
		
			
	}

}
